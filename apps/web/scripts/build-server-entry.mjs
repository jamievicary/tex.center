#!/usr/bin/env node
// Bundle `src/server.ts` into `build/server.js`, sitting alongside
// adapter-node's `build/handler.js`. The custom entry hijacks HTTP
// Upgrade requests for the control-plane WebSocket proxy and
// delegates everything else to adapter-node's handler. See
// `src/server.ts` and `src/lib/server/wsProxy.ts`.
//
// esbuild is available transitively via vite; we don't list it as
// a direct devDep to avoid lockfile churn. The bundle uses
// `./handler.js` as an external so the import is preserved
// literally and resolves to the sibling file at runtime.

import { build } from "esbuild";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const appRoot = resolve(here, "..");

await build({
  entryPoints: [resolve(appRoot, "src/server.ts")],
  outfile: resolve(appRoot, "build/server.js"),
  bundle: true,
  format: "esm",
  platform: "node",
  target: "node20",
  // `./handler.js` is generated by `vite build` (adapter-node) and
  // sits next to `build/server.js` post-bundle. Keep it external
  // so the import literal survives bundling.
  external: ["./handler.js"],
  logLevel: "info",
});
