"""Regression: `node_modules` must not be tracked in git.

Iter 158 wired the M8.pw.4 live-pipeline job into the deploy
workflow — the first GitHub Actions step in this repo to run
`pnpm install` directly on a runner. It immediately failed with:

    ERR_PNPM_ENOENT  ENOENT: no such file or directory, mkdir
    '/home/runner/work/tex.center/tex.center/node_modules'

Root cause: `node_modules` had been tracked as a *symlink* in git
since iteration 4. On the maintainer's WSL2 working tree
`tests_normal/setup_node.sh` points it at an ext4-backed cache
under `$HOME/.cache/tex-center-nm/<hash>/node_modules` to avoid
DrvFs handle races. The symlink target is a per-machine path that
doesn't exist on a fresh GH Actions runner, so after `actions/
checkout@v5` lays it down `pnpm install` tries to `mkdir` through
the dangling link and ENOENTs.

The lock: assert no entry named `node_modules` is tracked in git
at any depth. setup_node.sh continues to create the symlink in
the working tree; .gitignore keeps it untracked.
"""

from __future__ import annotations

import subprocess
import unittest
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]


class TestNoTrackedNodeModules(unittest.TestCase):
    def test_no_node_modules_tracked(self) -> None:
        out = subprocess.run(
            ["git", "ls-files", "-z"],
            cwd=ROOT,
            check=True,
            capture_output=True,
        )
        tracked = [p for p in out.stdout.split(b"\x00") if p]
        offenders = [
            p.decode("utf-8", errors="replace")
            for p in tracked
            if b"node_modules" in p.split(b"/")
        ]
        self.assertEqual(
            offenders,
            [],
            f"node_modules entries are tracked in git: {offenders!r}. "
            "These break `pnpm install` on fresh clones (including CI "
            "runners) when committed as symlinks pointing into a "
            "maintainer-local cache. setup_node.sh creates the "
            "symlink in the working tree; it must stay untracked.",
        )

    def test_node_modules_symlink_is_gitignored(self) -> None:
        # The harness commits with `git add -A`. If `node_modules`
        # is untracked-in-index but the working tree symlink is
        # not matched by .gitignore, `add -A` re-stages it. The
        # rule `node_modules/` (trailing slash) matches dirs
        # only, not the symlink setup_node.sh creates here. This
        # asserts the gitignore rule actually catches the symlink
        # — caught a regression in iter 160 where iter 159's
        # `git rm --cached` was silently undone every commit.
        symlink = ROOT / "node_modules"
        if not symlink.is_symlink():
            self.skipTest("node_modules symlink not present (CI clone)")
        out = subprocess.run(
            ["git", "check-ignore", "-v", "node_modules"],
            cwd=ROOT,
            capture_output=True,
        )
        self.assertEqual(
            out.returncode,
            0,
            f"node_modules symlink is not gitignored. "
            f"stdout={out.stdout!r} stderr={out.stderr!r}. "
            "The rule must match the working-tree symlink (not "
            "just a node_modules/ directory), otherwise the "
            "harness's `git add -A` re-stages it.",
        )

    def test_no_tracked_symlinks_outside_vendor(self) -> None:
        # Tracked symlinks pointing at absolute or escape-the-repo
        # targets are the same class of bug as the node_modules one
        # — they only work on the maintainer's machine. Vendor
        # submodules legitimately use symlinks for internal layout,
        # so scope the check to the rest of the tree.
        out = subprocess.run(
            ["git", "ls-files", "-s", "-z"],
            cwd=ROOT,
            check=True,
            capture_output=True,
        )
        offenders: list[str] = []
        for entry in out.stdout.split(b"\x00"):
            if not entry:
                continue
            # Each entry: "<mode> <oid> <stage>\t<path>"
            try:
                meta, path = entry.split(b"\t", 1)
            except ValueError:
                continue
            mode = meta.split(b" ", 1)[0]
            if mode != b"120000":
                continue
            path_s = path.decode("utf-8", errors="replace")
            if path_s.startswith("vendor/"):
                continue
            offenders.append(path_s)
        self.assertEqual(
            offenders,
            [],
            f"Tracked symlinks outside vendor/: {offenders!r}. "
            "Symlinks committed at the repo root almost always "
            "point at maintainer-local paths and break on CI.",
        )
